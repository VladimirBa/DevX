package com.java.class02;

public class DtaTypesPractice {
    public static void main(String[] args) {
        int num1 = 10;
        byte num2 = 32;
        short num3 = 54;
        long num4 = 33;
        float num5 = 5.6f;
        double num6 = 48.09;
        // String we've to surround with double quotes
        String num7 = "This is my Java program.";
        //Character we've to surround with single quotes
        char num8 = '+';
        //We can store only true or false into boolean variable
        boolean num9 = false;
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(num5);
        System.out.println(num6);
        System.out.println(num7);
        System.out.println(num8);
        System.out.println(num9);

    }
}
